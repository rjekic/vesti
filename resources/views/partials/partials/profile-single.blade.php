			<h4>Nikola Radojičić</h4>
			<p>Stanko Terzić je rođen 1952. godine u Valjevu, gde je završio osnovnu školu i Valjevsku gimnaziju, a na Prirodno-matematičkom fakultetu u Beogradu je diplomirao 1975. godine. Radio je najpre kao profesor matematike i pomoćnik direktora Poljoprivredne škole u Valjevu, potom kao prosvetni savetnik za matematiku u Međuopštinskom zavodu za unapređenje vaspitanja i obrazovanja Valjevo i kao samostalni programer-rukovodilac odseka obrade u SDK.</p>
 
			<p>Od 1993. do 1994. godine obavljao je dužnost predsednika Izvršnog odbora Skupštine opštine Valjevo, a od 1994. do 2001. godine bio je načelnik Kolubarskog okruga. Radio je kao stručni saradnik u Službi za platni promet, ekspozituri Narodne banke Srbije u Valjevu, a od 2005. do 2007. godine bio je direktor Javne ustanove Dom kulture „Valjevo“. Bio je predsednik OO SPS u Valjevu, savezni poslanik u prvom višestranačkom skupštinskom sazivu nekadašnje SRJ i predsednik UO JP „Skloništa Srbije“.</p>
 
			<p>Od 2008. do 2012. godine bio zamenik gradonačelnika Valjeva.</p>
			 
